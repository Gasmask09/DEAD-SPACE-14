using System;
using Content.Shared.DeadSpace.CCCCVars;
using Content.Shared.CCVar;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface;
using Robust.Shared.Configuration;
using Robust.Shared.IoC;

namespace Content.Client.Info;

[GenerateTypedNameReferences]
public sealed partial class RulesLinksSection : BoxContainer
{
    public RulesLinksSection()
    {
        RobustXamlLoader.Load(this);

        var uriOpener = IoCManager.Resolve<IUriOpener>();
        var cfg = IoCManager.Resolve<IConfigurationManager>();

        DiscordButton.OnPressed += args => uriOpener.OpenUri(cfg.GetCVar(CCVars.InfoLinksDiscord));
        DiscordButton.Visible = cfg.GetCVar(CCVars.InfoLinksDiscord) != "";
        RolesButton.OnPressed += args => uriOpener.OpenUri(cfg.GetCVar(CCCCVars.InfoLinksRoles));
        RolesButton.Visible = cfg.GetCVar(CCCCVars.InfoLinksRoles) != "";
        LawsButton.OnPressed += args => uriOpener.OpenUri(cfg.GetCVar(CCCCVars.InfoLinksLaws));
        LawsButton.Visible = cfg.GetCVar(CCCCVars.InfoLinksLaws) != "";
    }
}

